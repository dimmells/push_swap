void		shift_down(int **stack, int size)
{
	int		i;
	int		last;

	last = (*stack)[size - 1];
	i = size;
	while (i > 0)
	{
		(*stack)[i] = (*stack)[i - 1];
		i--;
	}
	(*stack)[0] = last;
}
