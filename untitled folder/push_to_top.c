void		push_to_top(int **a, int **b, int *size_a, int *size_b)
{
	int		i;
	int		top;

	top = (*a)[0];
	i = 0;
	while (i < *size_a - 1)
	{
		(*a)[i] = (*a)[i + 1];
		i++;
	}
	*size_a -= 1;
	i = *size_b;
	while (i > 0)
	{
		(*b)[i] = (*b)[i - 1];
		i--;
	}
	(*b)[0] = top;
	*size_b += 1;
}
